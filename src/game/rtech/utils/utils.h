inline const std::string fourCCToString(int32_t n)
{
    std::stringstream ss;
    ss << (char)((n & 0x000000ff) >> 0);
    ss << (char)((n & 0x0000ff00) >> 8);
    ss << (char)((n & 0x00ff0000) >> 16);
    ss << (char)((n & 0xff000000) >> 24);
    return ss.str();
}

